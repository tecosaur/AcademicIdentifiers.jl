# Quine (ish) for automatically setting hyphenation data
# (ish): Technically this is a cheating quine, as it relies on receiving input.

# Execute with `sh isbn-hyphenation.jl` to update the hyphenation data.

#=
awk '/^#=run/{flag=1;next}/=#/{flag=0}flag{gsub(/__FILE__/,"\"'"$0"'\"");print}' "$0" | \
  julia --startup-file=no -E 'readchomp("/dev/fd/0") |> Meta.parse |> eval' && echo "Done"; exit
=#

#=run
begin
using Pkg
Pkg.activate(temp=true)
Pkg.add("XML")

using XML
using Dates
using Downloads

hversion = IOBuffer()
Downloads.request("https://www.isbn-international.org/bl_proxy/GetRangeInformations",
  input = IOBuffer("format=1&language=en&translatedTexts=Printed%3BLast+Change"),
  output = hversion, method = "POST", headers = ["Content-Type" => "application/x-www-form-urlencoded"])
res = read(seekstart(hversion), String)
hvalue, hfilename = match(r"\"value\": *\"(\d+)\", *\"filename\": *\"(.+?)\"", res).captures

hdata = IOBuffer()
Downloads.download("https://www.isbn-international.org/download_range/$hvalue/$hfilename", hdata)
hxml = XML.read(seekstart(hdata), LazyNode)

function getchild(node, ctag)
  for child in children(node)
    if tag(child) == ctag
      return child
    end
  end
  nothing
end

grouphinfo = Pair{Int, Vector{Pair{Int, UnitRange{Int}}}}[]
for node in children(getchild(getchild(hxml, "ISBNRangeMessage"), "EAN.UCCPrefixes"))
    prefix = getchild(getchild(node, "Prefix"), nothing).value
    rules = Pair{Int, UnitRange{Int}}[]
    for rule in children(getchild(node, "Rules"))
        range = getchild(getchild(rule, "Range"), nothing).value
        rstart, rstop = split(range, '-', limit=2)
        length = parse(Int, getchild(getchild(rule, "Length"), nothing).value)
        push!(rules, length => parse(Int, rstart):parse(Int, rstop))
    end
    push!(grouphinfo, parse(Int, prefix) => rules)
end

pubhinfo = Dict{Int16, Vector{Pair{Int, Vector{Pair{Int, UnitRange{Int}}}}}}()
for node in children(getchild(getchild(hxml, "ISBNRangeMessage"), "RegistrationGroups"))
    prefix = getchild(getchild(node, "Prefix"), nothing).value
    agency = getchild(getchild(node, "Agency"), nothing).value
    rules = Pair{Int, UnitRange{Int}}[]
    for rule in children(getchild(node, "Rules"))
        range = getchild(getchild(rule, "Range"), nothing).value
        rstart, rstop = split(range, '-', limit=2)
        length = parse(Int, getchild(getchild(rule, "Length"), nothing).value)
        push!(rules, length => parse(Int, rstart):parse(Int, rstop))
    end
    p3, prest = if '-' in prefix
        split(prefix, '-', limit=2)
    else
        @warn "Prefix $prefix has no hyphen!"
        prefix, "-1"
    end
    p3num = parse(Int16, p3)
    if !haskey(pubhinfo, p3num)
        pubhinfo[p3num] = Pair{Int, Vector{Pair{Int, UnitRange{Int}}}}[]
    end
    push!(pubhinfo[p3num], parse(Int, prest) => rules)
end
map!(sort!, values(pubhinfo))
pubhvinfo = sort!(collect(pubhinfo))

const SENTINEL = "\n## GENERATED CODE BEYOND THIS POINT ##"
const PREAMBLE = readuntil(__FILE__, SENTINEL, keep=true)

out = IOBuffer()
write(out, PREAMBLE, "\n\n# ISBN Hyphenation rules as of $(today())\n\n")

print(out, "const ISBN_GROUP_HYPHENATION = NTuple{$(length(grouphinfo)), $(eltype(grouphinfo))}((")
for (prefix, rules) in grouphinfo
    print(out, "\n  $prefix => [")
    for (length, range) in rules
        print(out, "\n    $length => $range,")
    end
    print(out, "\n  ],")
end
print(out, "\n))\n\n")

print(out, "const ISBN_PUB_HYPHENATION = NTuple{$(length(pubhvinfo)), $(eltype(pubhvinfo))}((")
for (p3, pfull) in pubhvinfo
    print(out, "\n  $p3 => [")
    for (prest, rules) in pfull
        print(out, "\n    $prest => [")
        for (length, range) in rules
            print(out, "\n      $length => $range,")
        end
        print(out, "\n    ],")
    end
    print(out, "\n  ],")
end
print(out, "\n))\n")

open(io -> write(io, seekstart(out)), __FILE__, "w")

end
